-- Breaking out Address into Individual Column (Address, City, State)
SELECT PROPERTYADDRESS
FROM NASHVILLEHOUSING;

SELECT 
    SUBSTR(PROPERTYADDRESS, 1, INSTR(PROPERTYADDRESS, ',')-1) ADDRESS
    , SUBSTR(PROPERTYADDRESS, INSTR(PROPERTYADDRESS, ',') + 1, LENGTH(PROPERTYADDRESS)) CITY
FROM NASHVILLEHOUSING;

ALTER TABLE NASHVILLEHOUSING
ADD PROPERTYSPLITADDRESS VARCHAR(255);

UPDATE NASHVILLEHOUSING
SET PROPERTYSPLITADDRESS = SUBSTR(PROPERTYADDRESS, 1, INSTR(PROPERTYADDRESS, ',')-1);

