-- Change Y and N to Yes and No in "Sold as Vacant" Field
SELECT 
    SOLDASVACANT,
    CASE 
        WHEN SOLDASVACANT = 'Y' THEN 'Yes'
        WHEN SOLDASVACANT = 'N' THEN 'No'
        ELSE SOLDASVACANT
    END SOLDCHANGE
FROM NASHVILLEHOUSING;

UPDATE NASHVILLEHOUSING
SET SOLDASVACANT = CASE 
        WHEN SOLDASVACANT = 'Y' THEN 'Yes'
        WHEN SOLDASVACANT = 'N' THEN 'No'
        ELSE SOLDASVACANT
    END;

