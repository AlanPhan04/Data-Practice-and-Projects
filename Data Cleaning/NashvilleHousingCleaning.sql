-- Remove Duplicates
CREATE VIEW T AS(
    SELECT NASHVILLEHOUSING.*, ROW_NUMBER() OVER (
        PARTITION BY PARCELID
            , PROPERTYADDRESS
            , SALEPRICE
            , SALEDATE
            , LEGALREFERENCE 
        ORDER BY UNIQUEID_) ROW_NUM
    FROM NASHVILLEHOUSING
);
DELETE TABLE T
WHERE ROW_NUM>1;

SELECT PARCELID, PROPERTYADDRESS, SALEPRICE, SALEDATE, LEGALREFERENCE, COUNT(*)
FROM NASHVILLEHOUSING
GROUP BY PARCELID, PROPERTYADDRESS, SALEPRICE, SALEDATE, LEGALREFERENCE
ORDER BY COUNT(*) DESC;

ALTER TABLE NASHVILLEHOUSING
ADD COUNT INT;

UPDATE NASHVILLEHOUSING N
SET COUNT = (SELECT ROW_NUMBER() OVER (
        PARTITION BY PARCELID
            , PROPERTYADDRESS
            , SALEPRICE
            , SALEDATE
            , LEGALREFERENCE 
        ORDER BY UNIQUEID_)
        FROM NASHVILLEHOUSING);

