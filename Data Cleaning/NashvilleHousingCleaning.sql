-- Populate Property Address data

SELECT *
FROM NASHVILLEHOUSING
--WHERE PROPERTYADDRESS IS NULL
ORDER BY PARCELID;

SELECT N1.PARCELID, N1.PROPERTYADDRESS, N2.PARCELID, N2.PROPERTYADDRESS, COALESCE(N1.PROPERTYADDRESS, N2.PROPERTYADDRESS)
FROM NASHVILLEHOUSING N1
    JOIN NASHVILLEHOUSING N2
        ON N1.PARCELID = N2.PARCELID
            AND N1.UNIQUEID_ <> N2.UNIQUEID_
WHERE N1.PROPERTYADDRESS IS NULL;


UPDATE NASHVILLEHOUSING N1
SET N1.PROPERTYADDRESS = (
    SELECT DISTINCT N2.PROPERTYADDRESS 
    FROM NASHVILLEHOUSING N2
    WHERE 1=1
        AND N1.PARCELID = N2.PARCELID 
        AND N1.UNIQUEID_<> N2.UNIQUEID_ 
        AND N2.PROPERTYADDRESS IS NOT NULL
    FETCH FIRST 1 ROW ONLY
)
WHERE N1.PROPERTYADDRESS IS NULL;

