ADD PROPERTYSPLITCITY VARCHAR(255);

UPDATE NASHVILLEHOUSING
SET PROPERTYSPLITCITY = SUBSTR(PROPERTYADDRESS, INSTR(PROPERTYADDRESS, ',') + 1, LENGTH(PROPERTYADDRESS));

